void main() {
  // Declare a set of integers
  var s = {10, 20, 30, 40, 50};

  // Access elements by index using elementAt()
  print('Element at index 0: ${s.elementAt(0)}'); // 10
  print('Element at index 1: ${s.elementAt(1)}'); // 20
  print('Element at index 2: ${s.elementAt(2)}'); // 30
  print('Element at index 3: ${s.elementAt(3)}'); // 40
  print('Element at index 3: ${s.elementAt(4)}'); // 50
}
